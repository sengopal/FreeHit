package com.debut.ellipsis.freehit.More.MoreMain;


public class MoreItem {

    private int mMoreIcon;

    private int mMoreName;

    public MoreItem(int MoreIcon, int MoreName) {
        mMoreIcon = MoreIcon;
        mMoreName = MoreName;

    }

    public int getmMoreIcon() {
        return mMoreIcon;
    }

    public int getmMoreName() {
        return mMoreName;
    }

}
