package com.debut.ellipsis.freehit.More.Series;


public class SeriesListItem {
    private int mSeriesIcon;

    private String mSeriesName;


    public SeriesListItem(int SeriesIcon, String SeriesName) {
        mSeriesIcon = SeriesIcon;
        mSeriesName = SeriesName;

    }

    public int getmSeriesIcon() {
        return mSeriesIcon;
    }

    public String getmSeriesName() {
        return mSeriesName;
    }

}
